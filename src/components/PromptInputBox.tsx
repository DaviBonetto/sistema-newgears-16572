interface PromptInputBoxProps {
  onSend?: (message: string, files?: File[]) => void;
  isLoading?: boolean;
  placeholder?: string;
  className?: string;
}
export const PromptInputBox = React.forwardRef<HTMLDivElement, PromptInputBoxProps>(
  ({ onSend = () => {}, isLoading = false, placeholder = "Type your message...", className }, ref) => {
    const [input, setInput] = React.useState("");
    const [files, setFiles] = React.useState<File[]>([]);
    const [filePreviews, setFilePreviews] = React.useState<{ [key: string]: string }>({});
    const [selectedImage, setSelectedImage] = React.useState<string | null>(null);
    const [isRecording, setIsRecording] = React.useState(false);
    const uploadInputRef = React.useRef<HTMLInputElement>(null);

    const processFile = (file: File) => {
      setFiles([file]);
      const reader = new FileReader();
      reader.onload = (e) => setFilePreviews({ [file.name]: e.target?.result as string });
      reader.readAsDataURL(file);
    };

    const handleRemoveFile = () => {
      setFiles([]);
      setFilePreviews({});
    };

    const handleSubmit = () => {
      if (input.trim() || files.length > 0) {
        onSend(input, files);
        setInput("");
        setFiles([]);
        setFilePreviews({});
      }
    };

    const hasContent = input.trim() !== "" || files.length > 0;

    return (
      <>
        <PromptInput
          value={input}
          onValueChange={setInput}
          isLoading={isLoading}
          onSubmit={handleSubmit}
          className={className}
          disabled={isLoading}
          ref={ref}
        >
          {files.length > 0 && (
            <div className="flex gap-2 p-2">
              {files.map((file, index) => (
                <div key={index} className="relative">
                  {filePreviews[file.name] && (
                    <div className="w-16 h-16 rounded-xl overflow-hidden">
                      <img src={filePreviews[file.name]} alt={file.name} className="w-full h-full object-cover" />
                      <button onClick={handleRemoveFile} className="absolute top-1 right-1 rounded-full bg-black/70 p-0.5">
                        <X className="h-3 w-3 text-white" />
                      </button>
                    </div>
                  )}
                </div>
              ))}
            </div>
          )}

          <PromptInputTextarea placeholder={placeholder} />

          <PromptInputActions className="flex justify-between pt-2">
            <div className="flex gap-1">
              <PromptInputAction tooltip="Upload">
                <button
                  onClick={() => uploadInputRef.current?.click()}
                  className="flex h-8 w-8 items-center justify-center rounded-full text-[#9CA3AF] hover:bg-gray-600/30"
                  disabled={isRecording}
                >
                  <Paperclip className="h-5 w-5" />
                  <input
                    ref={uploadInputRef}
                    type="file"
                    className="hidden"
                    onChange={(e) => e.target.files?.[0] && processFile(e.target.files[0])}
                    accept="*/*"
                  />
                </button>
              </PromptInputAction>
            </div>

            <PromptInputAction tooltip={isLoading ? "Stop" : hasContent ? "Send" : "Voice"}>
              <Button
                variant="default"
                size="icon"
                className={cn(hasContent ? "bg-white text-black" : "bg-transparent text-[#9CA3AF]")}
                onClick={handleSubmit}
                disabled={isLoading}
              >
                {isLoading ? <Square className="h-4 w-4" /> : hasContent ? <ArrowUp className="h-4 w-4" /> : <Mic className="h-5 w-5" />}
              </Button>
            </PromptInputAction>
          </PromptInputActions>
        </PromptInput>

        <ImageViewDialog imageUrl={selectedImage} onClose={() => setSelectedImage(null)} />
      </>
    );
  }
);
PromptInputBox.displayName = "PromptInputBox";
